export const getTranslation = (workArray, currentItem, workColumn = "translation") => {
    
    let workVar;
    if (currentItem === -1) {
        workVar = workArray
    } else {
        workVar = workArray[currentItem][workColumn];
    }
    
    const workCol = workVar.split("--")
        .map(part => part.split(":").map(s => s.trim())) // –†–∞–∑–±–∏–≤–∞–µ–º –∏ —É–¥–∞–ª—è–µ–º –ø—Ä–æ–±–µ–ª—ã
        .reduce((acc, [key, value]) => ({ ...acc, [key]: value }), {}); // –°–æ–∑–¥–∞—ë–º –æ–±—ä–µ–∫—Ç {ru: "...", ua: "..."}

    if (i18n.language === "en") {
        return workCol["ua"] || workCol["ru"] || workVar;
    }

    // –ï—Å–ª–∏ —è–∑—ã–∫ –Ω–µ "en", –ø—Ä–æ–±—É–µ–º –Ω–∞–π—Ç–∏ –µ–≥–æ, –∏–Ω–∞—á–µ –±–µ—Ä—ë–º "ru" –∏–ª–∏ fallback
    return workCol[i18n.language] || workCol["ua"] || workCol["ru"] || workVar;
}